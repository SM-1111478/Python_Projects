# create a tuple with an integer, a float, a string, a boolean, and another tuple
tuple = (42, 3.4, "Python", True, (1, 2, 3))

# print the entire tuple
print(tuple)

# print the third element
print(tuple[2])

# print the first element in the nested tuple
print(tuple[4][0])